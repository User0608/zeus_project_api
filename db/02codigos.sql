--- tablas para los codigos ...
create table categoria_codigo(
    index varchar(4) not null,
    title varchar(80) not null
);
alter table categoria_codigo
    add constraint pk_categoria_codigo primary key (index);

create table codigo_item(
    codigo varchar(6) not null ,
    detalle varchar(80) not null,
    categoria_codigo_index varchar(4) not null
);
alter table codigo_item
    add constraint  pk_codigo_item primary key (codigo),
    add  constraint fk_codigo_item__categoria_codigo
        foreign key (categoria_codigo_index) references  categoria_codigo(index);
